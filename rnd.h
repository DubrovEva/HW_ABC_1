#ifndef __rnd__
#define __rnd__

#include <cstdlib>

//------------------------------------------------------------------------------
// rnd.h - содержит генератор случайных действительных чисел
//------------------------------------------------------------------------------

inline double Random() {
    return (double) (rand() % 10000000) / (double) (rand() % 1000000 + 1);
}

#endif //__rnd__
